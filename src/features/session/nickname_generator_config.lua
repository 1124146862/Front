local function utf8_len(s)
    if type(s) ~= "string" then
        return 0
    end
    local _, count = string.gsub(s, "[^\128-\193]", "")
    return count
end

local function is_valid_name(s, max_len)
    return type(s) == "string" and s ~= "" and utf8_len(s) <= max_len
end

local function uniq(list)
    local out = {}
    local seen = {}
    for _, v in ipairs(list or {}) do
        if type(v) == "string" and v ~= "" and not seen[v] then
            seen[v] = true
            out[#out + 1] = v
        end
    end
    return out
end

local function push_unique(out, seen, value, max_len)
    if is_valid_name(value, max_len) and not seen[value] then
        seen[value] = true
        out[#out + 1] = value
        return true
    end
    return false
end

local function rand_between(rng, a, b)
    if type(rng) == "function" then
        return rng(a, b)
    end
    return math.random(a, b)
end

local function pick(list, rng)
    if not list or #list == 0 then
        return nil
    end
    return list[rand_between(rng, 1, #list)]
end

local function expand_pool(pool, target_count, max_len)
    pool.prefixes = uniq(pool.prefixes)
    pool.suffixes = uniq(pool.suffixes)
    pool.bridges = uniq(pool.bridges)
    pool.singles = uniq(pool.singles)

    local out = {}
    local seen = {}

    for _, s in ipairs(pool.singles) do
        push_unique(out, seen, s, max_len)
    end

    if #out >= target_count then
        pool.singles = out
        pool.generated_count = #out
        return pool
    end

    for i = 1, #pool.prefixes do
        for j = 1, #pool.suffixes do
            if push_unique(out, seen, pool.prefixes[i] .. pool.suffixes[j], max_len) and #out >= target_count then
                pool.singles = out
                pool.generated_count = #out
                return pool
            end
        end
    end

    for i = 1, #pool.prefixes do
        for b = 1, #pool.bridges do
            for j = 1, #pool.suffixes do
                if push_unique(out, seen, pool.prefixes[i] .. pool.bridges[b] .. pool.suffixes[j], max_len) and #out >= target_count then
                    pool.singles = out
                    pool.generated_count = #out
                    return pool
                end
            end
        end
    end

    for i = 1, #pool.suffixes do
        for b = 1, #pool.bridges do
            for j = 1, #pool.prefixes do
                if push_unique(out, seen, pool.suffixes[i] .. pool.bridges[b] .. pool.prefixes[j], max_len) and #out >= target_count then
                    pool.singles = out
                    pool.generated_count = #out
                    return pool
                end
            end
        end
    end

    pool.singles = out
    pool.generated_count = #out
    return pool
end

local function build_all()
    for locale, pool in pairs(Config.pools) do
        expand_pool(pool, Config.target_single_count, Config.max_length)
        Config.pools[locale] = pool
    end
end

function Config.get_pool(locale)
    return Config.pools[locale] or Config.pools[Config.default_locale]
end

function Config.generate(locale, rng)
    local pool = Config.get_pool(locale)
    if not pool then
        return "Guest"
    end

    for _ = 1, 96 do
        local mode = rand_between(rng, 1, 100)
        local name

        if mode <= 45 then
            name = pick(pool.singles, rng)
        elseif mode <= 75 then
            name = (pick(pool.prefixes, rng) or "") .. (pick(pool.suffixes, rng) or "")
        elseif mode <= 92 then
            name = (pick(pool.prefixes, rng) or "") .. (pick(pool.bridges, rng) or "") .. (pick(pool.suffixes, rng) or "")
        else
            local a = pick(pool.prefixes, rng) or ""
            local b = pick(pool.prefixes, rng) or ""
            local c = pick(pool.suffixes, rng) or ""
            name = a .. b .. c
        end

        if is_valid_name(name, Config.max_length) then
            return name
        end
    end

    return pick(pool.singles, rng) or "Guest"
end

function Config.generate_batch(locale, count, rng)
    local result = {}
    local seen = {}
    count = tonumber(count) or 10

    for _ = 1, count * 8 do
        if #result >= count then
            break
        end
        local name = Config.generate(locale, rng)
        if name and not seen[name] then
            seen[name] = true
            result[#result + 1] = name
        end
    end

    while #result < count do
        result[#result + 1] = Config.generate(locale, rng)
    end

    return result
end

function Config.count(locale)
    local pool = Config.get_pool(locale)
    if not pool then
        return 0
    end
    return #(pool.singles or {})
end

function Config.seed(value)
    math.randomseed(value or os.time())
    math.random()
    math.random()
    math.random()
end

